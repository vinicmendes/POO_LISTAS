
package lista03.exercicio1;

public class calculadora{
    public calculadora(){
        
    }
    public int soma(int num1, int num2){
        return num1+num2;
    }
    public int subtrai(int num1, int num2){
        return num1-num2;
    }
    public int multiplica(int num1, int num2){
        return num1*num2;
    }
    public int divide(int num1, int num2){
        return num1/num2;
    }
 }
